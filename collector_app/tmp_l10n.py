for map_name, translations in lang_maps.items():
    marker = 'const Map<String, String> ' + map_name
    start = c.find(marker)
    if start == -1:
        print(f'{map_name} not found')
        continue
    closing = c.find('};', start)
    if closing == -1:
        print(f'Closing not found for {map_name}')
        continue
    new_keys = ''
    for k, v in translations.items():
        new_keys += f"  '{k}': '{v}',\n"
    c = c[:closing] + new_keys + '};' + c[closing+2:]
    print(f'{map_name} updated')

with open('lib/l10n/app_localizations.dart', 'w', encoding='utf-8') as f:
    f.write(c)
print('Done')
